#!/usr/bin/env python3
"""
Parse bitbake dependency graph output to extract SRC_URI and SRCREV per recipe.

Strategy (Tinfoil, default):
  1. Read pn-buildlist (from `bitbake -g`, ~570 recipes) as the recipe list.
  2. Use bitbake Tinfoil API to query SRC_URI/SRCREV in a single process.
     ~37s for ~570 recipes (vs ~17 min with bitbake -e × N).
     If pn-buildlist is absent, falls back to all_recipes() (~2750, slower).

Strategy (legacy, --pn-buildlist):
  1. Read pn-buildlist (from `bitbake -g`) to get the list of recipes.
  2. For each recipe, run `bitbake -e <recipe>` and parse its SRC_URI / SRCREV.
  3. Filter for git:// based sources and output JSON.

Usage:
    # Preferred: Tinfoil (requires bitbake environment)
    python3 parse_bitbake_deps.py --build-dir <path> --machine <machine>

    # Legacy fallback: bitbake -e per recipe
    python3 parse_bitbake_deps.py --pn-buildlist <path> --machine <machine>

Output: JSON array to stdout, one entry per git-based recipe.
"""

import argparse
import glob
import json
import os
import re
import signal
import subprocess
import sys


def parse_args():
    parser = argparse.ArgumentParser(
        description="Parse bitbake dependency graph to extract git-based SRC_URI and SRCREV."
    )
    # Tinfoil mode (default)
    parser.add_argument(
        "--build-dir",
        default=None,
        help="Path to bitbake build directory (containing conf/bblayers.conf). "
             "Enables Tinfoil mode (fast, single process).",
    )
    # Legacy mode (fallback)
    parser.add_argument(
        "--pn-buildlist",
        default=None,
        help="Path to pn-buildlist file generated by 'bitbake -g'. "
             "Enables legacy mode (bitbake -e per recipe, slower).",
    )
    parser.add_argument(
        "--machine",
        required=True,
        help="Target machine name (for metadata only).",
    )
    return parser.parse_args()


# === Shared helpers ===


def extract_git_srcuris(src_uri_value):
    """
    Extract git:// URIs from a SRC_URI value.

    SRC_URI can contain multiple space-separated entries.
    We only care about git:// entries.

    Returns list of git URIs.
    """
    if not src_uri_value:
        return []

    entries = src_uri_value.split()
    git_entries = []
    current = None

    for entry in entries:
        if entry.startswith("git://"):
            if current:
                git_entries.append(current)
            current = entry
        elif current and "://" not in entry:
            # Append bare parameters (no scheme) to the current git URI.
            # Entries with a scheme (file://, http://, etc.) are separate
            # fetcher URIs and must not be appended.
            current = current + " " + entry
        # Non-git standalone entries (other scheme URIs) are ignored

    if current:
        git_entries.append(current)

    return git_entries


def extract_repo_name(git_uri):
    """
    Extract repository name from a git URI.

    git://github.com/openbmc/linux;protocol=https;branch=dev-6.6 -> linux
    """
    url_part = git_uri.split(";")[0]
    path = url_part.split(":")[-1]
    if path.startswith("//"):
        path = path[2:]

    repo_name = path.split("/")[-1]
    if repo_name.endswith(".git"):
        repo_name = repo_name[:-4]

    return repo_name


def _to_ssh_url(host, path):
    """Convert host + path to git@host:path SSH URL, stripping any port."""
    if ":" in host:
        host = host.split(":")[0]
    return "git@" + host + ":" + path


def _parse_git_uri_params(git_uri):
    """Parse ``;key=value`` parameters from a BitBake git URI."""
    params = {}
    for param in git_uri.split(";")[1:]:
        if "=" not in param:
            continue
        key, value = param.split("=", 1)
        params[key] = value.strip('"').strip("'")
    return params


def _resolve_git_uri_srcrev(git_uri, default_srcrev, get_var):
    """Resolve the revision that belongs to a single git URI entry."""
    params = _parse_git_uri_params(git_uri)

    # URI-local rev pin wins over recipe-wide SRCREV variables.
    if params.get("rev"):
        return params["rev"]

    src_name = params.get("name")
    if src_name:
        named_srcrev = get_var("SRCREV_" + src_name)
        if named_srcrev:
            return named_srcrev

    return default_srcrev


def _detect_runtime_git_host():
    """Detect the runtime Git host used by init_openbmc_repo.sh.

    The init script copies ``meta-*/git-mirror-url.sh`` (or legacy
    ``meta-*/github-gitlab-url.sh``), rewrites ``GIT_MIRROR_HOST=...`` based
    on the current OpenBMC origin URL, and executes it. Reuse that same source
    here so deps.json generation follows the same environment-specific host
    choice.
    """
    cache = getattr(_detect_runtime_git_host, "_cache", None)
    if cache is not None:
        return cache

    repo_root = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
    openbmc_root = os.path.join(repo_root, "workspace", "openbmc")

    # Glob-discover the vendor mirror script (new name first, then legacy)
    runtime_script = ""
    for candidate in sorted(glob.glob(os.path.join(openbmc_root, "meta-*", "git-mirror-url.sh"))) \
                  + sorted(glob.glob(os.path.join(openbmc_root, "meta-*", "github-gitlab-url.sh"))):
        if os.path.isfile(candidate):
            runtime_script = candidate
            break

    host = ""
    if runtime_script:
        pattern = re.compile(r'^(GITLAB_IP|GIT_MIRROR_HOST)=["\']?([^"\'\n]+)')
        with open(runtime_script, "r", encoding="utf-8") as script_file:
            for line in script_file:
                match = pattern.match(line.strip())
                if match:
                    host = match.group(2).strip()
                    break

    if not host:
        git_config = os.path.join(openbmc_root, ".git", "config")
        if os.path.isfile(git_config):
            with open(git_config, "r", encoding="utf-8") as config_file:
                for raw_line in config_file:
                    line = raw_line.strip()
                    if not line.startswith("url = "):
                        continue
                    remote_url = line.split("=", 1)[1].strip()
                    if remote_url.startswith("git@"):
                        host = re.sub(r'^git@([^:]+):.*', r'\1', remote_url)
                        break
                    if remote_url.startswith(("http://", "https://")):
                        host = re.sub(r'^https?://([^/:]+).*', r'\1', remote_url)
                        break

    _detect_runtime_git_host._cache = host
    return host


def _load_runtime_bb_vars():
    """Load BitBake-style variables inferred from runtime init artifacts."""
    host = _detect_runtime_git_host()
    if not host:
        return {}
    return {"GIT_MIRROR_HOST": host}


def _is_private_host(host_port):
    """
    Return True if host_port refers to a private/internal server.

    Detection strategy (no hardcoded IPs or hostnames):

    1. **BitBake variable reference**: ``${GIT_MIRROR_HOST}``, ``${SOME_VAR}``
       — these always resolve to internal servers at runtime.
    2. **RFC 1918 private IPs**: 10.x.x.x, 172.16–31.x.x, 192.168.x.x
       — internal networks by definition.
    3. **Runtime init-script derived hosts**: host discovered from
       ``meta-*/git-mirror-url.sh`` or ``.git/config``.

    This keeps the source code free of any internal IPs or hostnames.
    """
    # Strip port for host-only checks
    host = host_port.split(":")[0] if ":" in host_port else host_port

    # 1) BitBake variable reference (e.g. ${GIT_MIRROR_HOST})
    if host.startswith("${") and host.endswith("}"):
        return True

    # 2) RFC 1918 private IP ranges
    import ipaddress
    try:
        addr = ipaddress.ip_address(host)
        if addr.is_private:
            return True
    except ValueError:
        pass  # not an IP → skip

    # 3) Runtime/init-script derived hosts
    runtime_host = _detect_runtime_git_host()
    if runtime_host and (host_port == runtime_host or host == runtime_host):
        return True

    return False


def derive_clone_url(git_uri):
    """
    Derive a clone-ready URL from a BitBake git URI.

    Routing rules (private IPs / config-file hosts always use SSH):

      git://<private-ip>:port/path;protocol=https  ->  git@<private-ip>:path
      git://<config-host>/path;protocol=https       ->  git@<config-host>:path
      git://github.com/...;protocol=https            ->  https://github.com/...
      git://git.infradead.org/...                    ->  git://git.infradead.org/...
      git://git@host/path                            ->  git@host:path (SSH shorthand)

    The ``ob`` script's clone logic uses this URL only when the local
    BitBake download mirror is NOT available.  When the mirror exists,
    it clones directly from the local mirror (zero network, zero auth).
    """
    parts = git_uri.split(";")
    url_part = parts[0]

    # --- Extract protocol parameter ---
    proto = ""
    for param in parts[1:]:
        if param.startswith("protocol="):
            proto = param.split("=", 1)[1].strip('"').strip("'")
            break

    if url_part.startswith("git://"):
        rest = url_part[6:]

        # Handle malformed git://git@host/path -> git@host:path (SSH)
        if rest.startswith("git@"):
            rest = rest[4:].lstrip("/")
            if ":/" in rest:
                host, path = rest.split(":/", 1)
                return "git@" + host.rstrip(":") + ":" + path.lstrip("/")
            first_slash = rest.find("/")
            if first_slash > 0:
                host = rest[:first_slash].rstrip(":")
                return "git@" + host + ":" + rest[first_slash + 1:]
            return "git@" + rest

        # Split host[:port] and path
        first_slash = rest.find("/")
        if first_slash < 0:
            # No path — shouldn't happen but handle gracefully
            return "git://" + rest
        host_port = rest[:first_slash]
        path = rest[first_slash + 1:]

        # --- Private/internal hosts: always use SSH (no auth needed) ---
        if _is_private_host(host_port):
            return _to_ssh_url(host_port, path)

        # --- External hosts: respect protocol= parameter ---

        # Handle host:path SSH shorthand (no numeric port)
        if ":" in host_port and not host_port.startswith("["):
            after_colon = host_port[host_port.index(":") + 1:]
            if after_colon and not after_colon[0].isdigit():
                # host:path -> rewrite to host/path
                host_port = host_port.replace(":", "/", 1)
                # recalculate rest after rewrite
                rest = host_port + "/" + path

        if proto == "https":
            return "https://" + (rest if ":" not in host_port or host_port == url_part[6:6+len(host_port)] else host_port + "/" + path)
        elif proto == "ssh":
            return _to_ssh_url(host_port, path)
        elif proto == "http":
            return "http://" + (rest if ":" not in host_port else host_port + "/" + path)
        elif proto and proto != "git":
            return proto + "://" + (rest if ":" not in host_port else host_port + "/" + path)

        # No protocol param or protocol=git — keep git:// as-is
        # Rebuild full URL in case host_port was rewritten
        return "git://" + host_port + "/" + path
    elif url_part.startswith("https://"):
        return url_part
    elif url_part.startswith("ssh://"):
        return url_part
    else:
        return url_part


def _build_result_entry(git_uri, srcrev, recipe):
    """Build a single deps.json entry from a git URI and metadata."""
    repo_name = extract_repo_name(git_uri)

    # Extract branch from SRC_URI parameters
    branch = ""
    for param in git_uri.split(";")[1:]:
        if param.startswith("branch="):
            branch = param.split("=", 1)[1].strip('"').strip("'")
            break

    return {
        "name": repo_name,
        "src_uri": git_uri,
        "srcrev": srcrev if srcrev not in ("AUTOINC", "INVALID", "${AUTOREV}") else "${AUTOREV}",
        "recipe": recipe,
        "clone_url": derive_clone_url(git_uri),
        "branch": branch,
    }


# === Tinfoil mode (fast, default) ===


def _resolve_bb_vars(tinfoil, text):
    """Resolve ${VAR} references in text using BitBake's config data store.

    Variables like GIT_MIRROR_HOST are defined in local.conf or bbclass files but
    are NOT expanded by d.getVar("SRC_URI") because they appear as literal
    strings inside the URI value. This function finds all ${VAR} patterns
    and resolves them from the global config data store.
    """
    if "${" not in text:
        return text

    # BitBake can preserve unresolved variables as escaped strings like
    # ``\${GIT_MIRROR_HOST}``. Normalize those first so replacement does not leave
    # a stray leading backslash in the host portion.
    text = text.replace(r"\${", "${")

    var_pattern = re.compile(r'\$\{([A-Za-z_][A-Za-z0-9_]*)\}')
    runtime_vars = _load_runtime_bb_vars()

    def _replacer(m):
        var_name = m.group(1)
        # 1. Try BitBake config data store
        try:
            val = tinfoil.config_data.getVar(var_name)
            if val:
                return val
        except Exception:
            pass
        # 2. Runtime fallback: same host discovered by init_openbmc_repo.sh
        if var_name in runtime_vars:
            return runtime_vars[var_name]
        return m.group(0)  # keep unresolved as-is

    return var_pattern.sub(_replacer, text)


def _extract_git_deps(src_uri, srcrev, recipe, seen_repos, results, get_var=None):
    """Extract git-based deps from a recipe's SRC_URI, dedup by repo name."""
    if get_var is None:
        get_var = lambda _name: ""

    git_uris = extract_git_srcuris(src_uri)
    for git_uri in git_uris:
        repo_name = extract_repo_name(git_uri)
        if repo_name in seen_repos:
            continue
        seen_repos.add(repo_name)
        results.append(
            _build_result_entry(
                git_uri,
                _resolve_git_uri_srcrev(git_uri, srcrev, get_var),
                recipe,
            )
        )


def query_deps_tinfoil(build_dir):
    """
    Use bitbake Tinfoil API to query SRC_URI/SRCREV in a single process.

    If pn-buildlist exists (from `bitbake -g`), only queries those ~570 recipes.
    Otherwise falls back to all_recipes() (slower, ~2750 target recipes).

    Returns list of deps entries for git-based recipes.
    """
    build_dir = os.path.abspath(build_dir)
    # build_dir is typically <openbmc>/build/<machine>
    openbmc_dir = os.path.dirname(os.path.dirname(build_dir))
    bitbake_lib = os.path.join(openbmc_dir, "bitbake", "lib")

    if not os.path.isdir(bitbake_lib):
        print(
            f"Error: bitbake lib not found at {bitbake_lib}. "
            f"Ensure --build-dir points to a valid build directory.",
            file=sys.stderr,
        )
        sys.exit(1)

    sys.path.insert(0, bitbake_lib)

    try:
        from bb.tinfoil import Tinfoil
    except ImportError:
        print(
            f"Error: cannot import bb.tinfoil from {bitbake_lib}. "
            f"Ensure bitbake is present in the OpenBMC tree.",
            file=sys.stderr,
        )
        sys.exit(1)

    # Determine recipe list: prefer pn-buildlist (target deps only, ~570)
    # over all_recipes() (~4492, even after filtering ~2750).
    pn_buildlist = os.path.join(build_dir, "pn-buildlist")
    if os.path.isfile(pn_buildlist):
        with open(pn_buildlist, "r", encoding="utf-8", errors="replace") as f:
            recipe_names = [line.strip() for line in f if line.strip()]
        print(
            f"Using pn-buildlist ({len(recipe_names)} recipes from bitbake -g).",
            file=sys.stderr,
        )
    else:
        recipe_names = None
        print(
            "pn-buildlist not found; will enumerate all recipes via Tinfoil.",
            file=sys.stderr,
        )

    results = []
    seen_repos = set()
    errors = 0

    # Tinfoil.prepare() spams stdout with cache/parsing logs.
    # Save real stdout, redirect to stderr during Tinfoil lifetime.
    real_stdout = sys.stdout
    sys.stdout = sys.stderr

    try:
        with Tinfoil() as tinfoil:
            print("Initializing Tinfoil (loading bitbake cache)...", file=sys.stderr)
            tinfoil.prepare(config_only=False)

            if recipe_names is not None:
                # Mode A: query only pn-buildlist recipes (~570)
                total = len(recipe_names)
                for i, pn in enumerate(recipe_names, 1):
                    print(
                        f"\rQuerying recipe {i}/{total}: {pn}...",
                        end="",
                        file=sys.stderr,
                    )
                    try:
                        d = tinfoil.parse_recipe(pn)
                    except Exception:
                        errors += 1
                        continue
                    if d is None:
                        errors += 1
                        continue
                    src_uri = _resolve_bb_vars(
                        tinfoil, d.getVar("SRC_URI") or ""
                    )
                    _extract_git_deps(
                        src_uri,
                        d.getVar("SRCREV") or "",
                        pn, seen_repos, results,
                        get_var=lambda var_name, data=d: data.getVar(var_name) or "",
                    )
            else:
                # Mode B: enumerate all_recipes(), skip native/sdk/cross variants
                # PN patterns: foo-native, foo-cross, foo-crosssdk (suffix)
                #              nativesdk-foo, cross-foo (prefix)
                # Both endswith and startswith checks are applied to catch all.
                _skip_suffix = ("-native", "-cross", "-crosssdk")
                _skip_prefix = ("nativesdk-", "cross-")
                recipe_list = [
                    r
                    for r in tinfoil.all_recipes()
                    if not (r.pn.endswith(_skip_suffix) or r.pn.startswith(_skip_prefix))
                ]
                total = len(recipe_list)
                print(f"Loaded {total} target recipes.", file=sys.stderr)

                for i, recipe in enumerate(recipe_list, 1):
                    pn = recipe.pn
                    print(
                        f"\rQuerying recipe {i}/{total}: {pn}...",
                        end="",
                        file=sys.stderr,
                    )
                    try:
                        d = tinfoil.parse_recipe(pn)
                    except Exception:
                        errors += 1
                        continue
                    if d is None:
                        errors += 1
                        continue
                    src_uri = _resolve_bb_vars(
                        tinfoil, d.getVar("SRC_URI") or ""
                    )
                    _extract_git_deps(
                        src_uri,
                        d.getVar("SRCREV") or "",
                        pn, seen_repos, results,
                        get_var=lambda var_name, data=d: data.getVar(var_name) or "",
                    )
    finally:
        sys.stdout = real_stdout

    print("", file=sys.stderr)  # newline after progress

    if errors > 0:
        print(
            f"Warning: {errors} recipes failed to parse (skipped).",
            file=sys.stderr,
        )

    return results


# === Legacy mode (bitbake -e per recipe, fallback) ===


def query_recipe_env(recipe_name):
    """
    Run `bitbake -e <recipe>` and extract SRC_URI and SRCREV.

    Returns dict with keys 'SRC_URI', 'SRCREV', and 'vars', or empty dict on failure.
    """
    try:
        result = subprocess.run(
            ["bitbake", "-e", recipe_name],
            capture_output=True,
            text=True,
            timeout=60,
        )
    except (subprocess.TimeoutExpired, FileNotFoundError):
        return {}

    if result.returncode != 0:
        return {}

    src_uri = ""
    srcrev = ""
    vars_map = {}

    for line in result.stdout.splitlines():
        # bitbake -e outputs VAR="value" lines for final variable values
        if line.startswith("SRC_URI="):
            m = re.match(r'^SRC_URI="(.*)"$', line)
            if m:
                src_uri = m.group(1).strip()
        elif line.startswith("SRCREV"):
            m = re.match(r'^(SRCREV(?:_[^=]+)?)="(.*)"$', line)
            if m:
                vars_map[m.group(1)] = m.group(2).strip()

    srcrev = vars_map.get("SRCREV", "")

    if not src_uri:
        return {}

    return {"SRC_URI": src_uri, "SRCREV": srcrev, "vars": vars_map}


def query_deps_legacy(pn_buildlist_path):
    """
    Legacy mode: read pn-buildlist and query each recipe via bitbake -e.

    Returns list of deps entries for git-based recipes.
    """
    if not os.path.isfile(pn_buildlist_path):
        print(f"Error: pn-buildlist not found: {pn_buildlist_path}", file=sys.stderr)
        sys.exit(1)

    with open(pn_buildlist_path, "r", encoding="utf-8", errors="replace") as f:
        recipe_names = [line.strip() for line in f if line.strip()]

    total = len(recipe_names)

    results = []
    seen_repos = set()

    for i, recipe in enumerate(recipe_names, 1):
        print(f"\rQuerying recipe {i}/{total}: {recipe}...", end="", file=sys.stderr)
        recipe_info = query_recipe_env(recipe)
        src_uri = recipe_info.get("SRC_URI", "")
        srcrev = recipe_info.get("SRCREV", "AUTOINC")
        vars_map = recipe_info.get("vars", {})

        _extract_git_deps(
            src_uri,
            srcrev,
            recipe,
            seen_repos,
            results,
            get_var=lambda var_name, data=vars_map: data.get(var_name, ""),
        )

    print("", file=sys.stderr)  # newline after progress
    return results


# === Main ===


def main():
    args = parse_args()

    # On SIGINT (ctrl+C), exit silently without writing partial output.
    # The caller (ob) writes to a .tmp file and renames on success,
    # so a signal here means the .tmp gets discarded.
    signal.signal(signal.SIGINT, lambda _sig, _frame: sys.exit(130))

    if args.build_dir:
        # Tinfoil mode (fast)
        results = query_deps_tinfoil(args.build_dir)
    elif args.pn_buildlist:
        # Legacy mode (fallback)
        results = query_deps_legacy(args.pn_buildlist)
    else:
        print(
            "Error: either --build-dir (Tinfoil, recommended) "
            "or --pn-buildlist (legacy) must be specified.",
            file=sys.stderr,
        )
        sys.exit(1)

    json.dump(results, sys.stdout, indent=2, ensure_ascii=False)
    print()


if __name__ == "__main__":
    main()
